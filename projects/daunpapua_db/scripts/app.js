let stations = [];
let photos = [];
let selectedPolygon = null;
let isobathLayer = null;
let currentFilteredStations = [];

const stationFile = "data/stations.csv";
const photoFile = "data/photos.csv";
const isobathFile = "data/isobaths.geojson";

// -------------------------
// 1. Normalisation functions
// -------------------------

function normalizeStation(d) {
  return {
    station: String(d.STATION || "").trim(),
    datetime_start: d.DATETIME_START,
    datetime_stop: d.DATETIME_STOP,
    date: String(d.DATETIME_START || "").slice(0, 10), // YYYY-MM-DD; ignores time
    latitude_start: Number(d.decimal_lat_start),
    latitude_stop: Number(d.decimal_lat_stop),
    longitude_start: Number(d.decimal_lon_start),
    longitude_stop: Number(d.decimal_lon_stop), // fixed: was decimal_lat_stop
    depth_start: Number(d.PROFONDEUR_START),
    depth_stop: Number(d.PROFONDEUR_STOP),
    tool: d.TOOL,
    metadata: d.METADATA || ""
  };
}

function normalizePhoto(p) {
  const keywordText = p.caption || p.keywords || p.KEYWORDS || p.Keyword || p.keyword || "";

  const rawPhoto = String(p.photo || p.PHOTO || "").trim();

  return {
    station: String(p.STATION || "").trim(),
    photo: rawPhoto ? `photos/${rawPhoto.replace(/^\/+/, "")}` : "",
    caption: String(keywordText || "").trim(),
    keywords: String(keywordText || "").trim()
  };
}

function photosForStation(stationCode) {
  return photos.filter(p => p.station === stationCode && p.photo !== "");
}

function stationHasPhotos(stationCode) {
  return photosForStation(stationCode).length > 0;
}

function keywordsForStation(stationCode) {
  return photosForStation(stationCode)
    .map(p => p.keywords || p.caption || "")
    .filter(Boolean)
    .join("; ");
}

function parseKeywordTerms(keywordText) {
  return String(keywordText || "")
    .toLowerCase()
    .split(/[\s,;]+/)
    .map(x => x.trim())
    .filter(Boolean);
}

function stationMatchesKeywords(stationCode, keywordText) {
  const terms = parseKeywordTerms(keywordText);
  if (terms.length === 0) return true;

  const stationKeywords = keywordsForStation(stationCode).toLowerCase();

  // AND logic: every entered keyword must be present in the station photo captions.
  return terms.every(term => stationKeywords.includes(term));
}

