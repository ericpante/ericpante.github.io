// 3. Load data
// -------------------------

Promise.all([
  new Promise(resolve => Papa.parse(stationFile, {
    download: true,
    header: true,
    dynamicTyping: false,
    skipEmptyLines: true,
    complete: results => resolve(results.data.map(normalizeStation))
  })),

  new Promise(resolve => Papa.parse(photoFile, {
    download: true,
    header: true,
    dynamicTyping: false,
    skipEmptyLines: true,
    complete: results => resolve(results.data.map(normalizePhoto))
  }))
]).then(([stationData, photoData]) => {
  stations = stationData;
  photos = photoData;
  initialiseFilters();
  filterStations();
});

