function photoGallery(stationCode) {
  const stationPhotos = photosForStation(stationCode);

  if (stationPhotos.length === 0) {
    return "<p>No photos for this station.</p>";
  }

  return `
    <div class="photo-gallery">
      ${stationPhotos.map(p => `
        <figure>
          <a href="${p.photo}" target="_blank">
            <img src="${p.photo}" alt="${escapeHtml(p.caption || stationCode)}">
          </a>
          <figcaption>${escapeHtml(p.caption)}</figcaption>
        </figure>
      `).join("")}
    </div>
  `;
}

// -------------------------
// 6. Filtering and display
// -------------------------

function filterStations() {
  const minDepth = Number(document.getElementById("minDepth").value || 0);
  const maxDepth = Number(document.getElementById("maxDepth").value || 10000);
  const selectedTool = document.getElementById("toolFilter").value;
  const selectedDate = document.getElementById("dateFilter").value;
  const selectedPhoto = document.getElementById("photoFilter").value;
  const keywordText = document.getElementById("keywordFilter").value.trim();
  const searchText = document.getElementById("stationSearch").value.trim().toLowerCase();

  const filtered = stations.filter(d => {
    const validCoords = !isNaN(d.latitude_start) && !isNaN(d.longitude_start);
    const depthOK = d.depth_start >= minDepth && d.depth_stop <= maxDepth;
    const toolOK = selectedTool === "all" || d.tool === selectedTool;
    const dateOK = selectedDate === "all" || d.date === selectedDate;
    const hasPhotos = stationHasPhotos(d.station);
    const photoOK = selectedPhoto === "all" ||
      (selectedPhoto === "available" && hasPhotos) ||
      (selectedPhoto === "not_available" && !hasPhotos);
    const keywordOK = stationMatchesKeywords(d.station, keywordText);
    const searchOK = searchText === "" || d.station.toLowerCase().includes(searchText);
    const polygonOK = !selectedPolygon || isPointInSelectedPolygon(d.latitude_start, d.longitude_start);

    return validCoords && depthOK && toolOK && dateOK && photoOK && keywordOK && searchOK && polygonOK;
  });

  currentFilteredStations = filtered;

  layerGroup.clearLayers();

  filtered.forEach(d => {
    L.marker([d.latitude_start, d.longitude_start])
      .bindPopup(`
        <b>${escapeHtml(d.station)}</b><br>
        Date: ${escapeHtml(d.date)}<br>
        Tool: ${escapeHtml(d.tool)}<br>
        Depth: ${d.depth_start}–${d.depth_stop} m<br>
        Photos: ${stationHasPhotos(d.station) ? "available" : "not available"}
      `)
      .addTo(layerGroup);
  });

  if (filtered.length > 0) {
    map.fitBounds(layerGroup.getBounds(), { padding: [20, 20], maxZoom: 10 });
  }

  document.getElementById("resultCount").textContent = `${filtered.length} station(s)`;
  renderResults(filtered);
}

function renderResults(filtered) {
  const resultsElement = document.getElementById("results");
  const viewModeElement = document.querySelector('input[name="resultView"]:checked');
  const viewMode = viewModeElement ? viewModeElement.value : "gallery";

  if (filtered.length === 0) {
    resultsElement.innerHTML = '<p>No station matches the current filters.</p>';
    return;
  }

  resultsElement.innerHTML = viewMode === "table" ? renderResultsTable(filtered) : renderResultsGallery(filtered);
}

