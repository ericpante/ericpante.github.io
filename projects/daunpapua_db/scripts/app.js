function isPointInSelectedPolygon(lat, lon) {
  const point = { lat: lat, lng: lon };

  // quick bounding-box test
  if (!selectedPolygon.getBounds().contains(point)) return false;

  const latlngs = selectedPolygon.getLatLngs();
  const ring = Array.isArray(latlngs[0]) ? latlngs[0] : latlngs;

  // ray-casting point-in-polygon test using lng as x, lat as y
  let inside = false;
  for (let i = 0, j = ring.length - 1; i < ring.length; j = i++) {
    const xi = ring[i].lng, yi = ring[i].lat;
    const xj = ring[j].lng, yj = ring[j].lat;
    const intersect = ((yi > lat) !== (yj > lat)) &&
      (lon < (xj - xi) * (lat - yi) / (yj - yi) + xi);
    if (intersect) inside = !inside;
  }
  return inside;
}

function formatNumber(x) {
  return Number.isFinite(x) ? x : "not available";
}

function escapeHtml(x) {
  return String(x || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}
