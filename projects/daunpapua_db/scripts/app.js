function initialiseFilters() {
  const toolSelect = document.getElementById("toolFilter");
  const dateSelect = document.getElementById("dateFilter");

  const tools = [...new Set(stations.map(d => d.tool).filter(Boolean))].sort();
  const dates = [...new Set(stations.map(d => d.date).filter(Boolean))].sort();

  toolSelect.innerHTML = '<option value="all">All tools</option>' +
    tools.map(x => `<option value="${escapeHtml(x)}">${escapeHtml(x)}</option>`).join("");

  dateSelect.innerHTML = '<option value="all">All dates</option>' +
    dates.map(x => `<option value="${escapeHtml(x)}">${escapeHtml(x)}</option>`).join("");
}

