function exportFilteredCSV() {
  if (!currentFilteredStations || currentFilteredStations.length === 0) {
    alert("No stations to export.");
    return;
  }

  const columns = [
    ["station", d => d.station],
    ["tool", d => d.tool],
    ["date", d => d.date],
    ["datetime_start", d => d.datetime_start],
    ["datetime_stop", d => d.datetime_stop],
    ["latitude_start", d => d.latitude_start],
    ["latitude_stop", d => d.latitude_stop],
    ["longitude_start", d => d.longitude_start],
    ["longitude_stop", d => d.longitude_stop],
    ["depth_start", d => d.depth_start],
    ["depth_stop", d => d.depth_stop],
    ["photos", d => stationHasPhotos(d.station) ? "available" : "not available"],
    ["photo_count", d => photosForStation(d.station).length],
    ["keywords", d => keywordsForStation(d.station)],
    ["metadata", d => d.metadata]
  ];

  const csv = [
    columns.map(([name]) => csvEscape(name)).join(","),
    ...currentFilteredStations.map(d => columns.map(([, getter]) => csvEscape(getter(d))).join(","))
  ].join("\n");

  const blob = new Blob([csv], { type: "text/csv;charset=utf-8;" });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = "filtered_stations.csv";
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

function csvEscape(value) {
  if (value === null || value === undefined || Number.isNaN(value)) return "";
  const text = String(value);
  if (/[",\n\r]/.test(text)) {
    return `"${text.replaceAll('"', '""')}"`;
  }
  return text;
}

function resetPolygon() {
  drawnItems.clearLayers();
  selectedPolygon = null;
  filterStations();
}

function resetFilters() {
  document.getElementById("minDepth").value = 0;
  document.getElementById("maxDepth").value = 10000;
  document.getElementById("toolFilter").value = "all";
  document.getElementById("dateFilter").value = "all";
  document.getElementById("photoFilter").value = "all";
  document.getElementById("keywordFilter").value = "";
  document.getElementById("stationSearch").value = "";
  resetPolygon();
}

