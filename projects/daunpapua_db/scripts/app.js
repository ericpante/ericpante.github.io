// 2. Map setup
// -------------------------

let map = L.map("map").setView([-5, 150], 5);
let layerGroup = L.featureGroup().addTo(map);
let drawnItems = new L.FeatureGroup().addTo(map);

// Fix Leaflet grey/partial rendering
setTimeout(() => {
  map.invalidateSize();
}, 300);

// Background layers. Esri imagery + CartoDB labels mimic the R leaflet example.
const esriImagery = L.tileLayer.provider("Esri.WorldImagery", { opacity: 0.8 });
const cartoLabels = L.tileLayer.provider("CartoDB.VoyagerOnlyLabels");
const osm = L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
  maxZoom: 18,
  attribution: "&copy; OpenStreetMap contributors"
});

esriImagery.addTo(map);
cartoLabels.addTo(map);


// Optional bathymetric contours, generated by scripts/create_isobaths.R.
// If data/isobaths.geojson is absent, the app continues normally.
fetch(isobathFile)
  .then(response => {
    if (!response.ok) throw new Error("No isobaths GeoJSON found");
    return response.json();
  })
  .then(geojson => {
    isobathLayer = L.geoJSON(geojson, {
      style: function () {
        return {
          color: "white",
          weight: 0.7,
          opacity: 0.8,
          fillOpacity: 0
        };
      },
      onEachFeature: function (feature, layer) {
        const level = feature.properties && feature.properties.level;
        if (level !== undefined && level !== null) {
          layer.bindTooltip(`${level} m`, { sticky: true });
        }
      }
    }).addTo(map);
  })
  .catch(() => {
    console.info("Bathymetric contours not loaded. Create data/isobaths.geojson with scripts/create_isobaths.R if needed.");
  });

const drawControl = new L.Control.Draw({
  draw: {
    polygon: true,
    rectangle: true,
    marker: false,
    circle: false,
    circlemarker: false,
    polyline: false
  },
  edit: {
    featureGroup: drawnItems,
    edit: true,
    remove: true
  }
});
map.addControl(drawControl);

L.control.layers(
  {
    "Esri imagery": esriImagery,
    "OpenStreetMap": osm
  },
  {
    "Labels": cartoLabels
  },
  { collapsed: true }
).addTo(map);

map.on(L.Draw.Event.CREATED, function (e) {
  drawnItems.clearLayers(); // keep only one selection polygon
  selectedPolygon = e.layer;
  drawnItems.addLayer(selectedPolygon);
  filterStations();
});

map.on(L.Draw.Event.EDITED, function () {
  selectedPolygon = drawnItems.getLayers()[0] || null;
  filterStations();
});

map.on(L.Draw.Event.DELETED, function () {
  selectedPolygon = null;
  filterStations();
});

